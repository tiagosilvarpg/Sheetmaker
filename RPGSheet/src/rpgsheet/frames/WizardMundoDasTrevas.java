    @SuppressWarnings("empty-statement")
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (Main.ficha.getExperiencia().getPontos()==0){
            passo++;
            
            if (passo==0){
                instrucoes.setText("Escolha um entre (fisico,social,mental) e gaste todos os pontos");
                Main.ficha.getExperiencia().increase(7*5);
            } else if (passo==1){
                instrucoes.setText("Escolha um entre (fisico,social,mental) e gaste todos os pontos, nao pode ser o mesmo que no passo anterior");
                Main.ficha.getExperiencia().increase(5*5);
            } else if (passo==2){
                instrucoes.setText("Escolha o que sobrou entre (fisico,social,mental) e gaste todos os pontos");
                Main.ficha.getExperiencia().increase(3*5);
            } else if (passo==3){
                instrucoes.setText("Escolha entre um entre (Talentos,Pericias,Conhecimentos) e gaste todos os pontos,nenhum pode ser maior que 3");
                Main.ficha.getExperiencia().increase(13*2);
            } else if (passo==4){
                instrucoes.setText("Escolha entre um entre (Talentos,Pericias,Conhecimentos) e gaste todos os pontos,nenhum pode ser maior que 3 e nao pode ser o mesmo que no passo anterior");
                Main.ficha.getExperiencia().increase(9*2);
            } else if (passo==5){
                instrucoes.setText("Escolha o que sobrou entre (Talentos,Pericias,Conhecimentos) e gaste todos os pontos,nenhum pode ser maior que 3");
                Main.ficha.getExperiencia().increase(5*2);
            } else if (passo==6){
                instrucoes.setText("Gaste os todos os pontos em Disciplinas, cheque as Disciplinas do seu cla no manual");
                Main.ficha.getExperiencia().increase(3*7);
            } else if (passo==7){
                instrucoes.setText("Gaste todos os pontos em Virtudes");
                Main.ficha.getExperiencia().increase(7*2);
            } else if (passo==8){
                instrucoes.setText("Gaste todos os pontos em Antecedentes");
                Main.ficha.getExperiencia().increase(5*1);
            } else if (passo==9){
                try {
                Main.ficha.findRecurso("Humanidade").setValor(    ((Atributo)(Main.ficha.findAtributo("Conciencia"))).getValor()
                                                                + ((Atributo)(Main.ficha.findAtributo("Auto-Controle"))).getValor());
                Main.ficha.findRecurso("Forca de Vontade").setValor( ((Atributo)(Main.ficha.findAtributo("Coragem"))).getValor());
                } catch (NullPointerException ex) {System.out.println("atributos faltando");};
                instrucoes.setText("Gaste o restante dos pontos como quiser e clique proximo para terminar");
                Main.ficha.getExperiencia().increase(15);
            } else dispose();            
            Main.refresh();
            
        }
        else JOptionPane.showMessageDialog(rootPane,"Voce tem pontos restantes");
    }//GEN-LAST:event_jButton1ActionPerformed

